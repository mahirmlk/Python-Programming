# Comparison Operators in Python
x = 10
y = 20

# Greater than (>) 
print("x > y:", x > y)  

# Less than (<) 
print("x < y:", x < y)  

# Equal to (==) 
print("x == y:", x == y)  

# Not equal to (!=) 
print("x != y:", x != y)  # 

# Greater than or equal to (>=)
print("x >= y:", x >= y)  

# Less than or equal to (<=)
print("x <= y:", x <= y)  # 

