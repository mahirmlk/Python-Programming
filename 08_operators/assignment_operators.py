# Assignment Operators in Python

# Basic assignment (=)
num = 10
print("Initial value:", num)  

num += 5
print("After +=:", num) 

# Subtract and assign (-=) 
num -= 3
print("After -=:", num)  

# Multiply and assign (*=) 
num *= 2
print("After *=:", num) 

# Divide and assign (/=) 
num /= 4
print("After /=:", num)  

# Floor divide and assign (//=)
num //= 2
print("After //=:", num)  

# Modulus and assign (%=)
num %= 2
print("After %=:", num)  

# Power and assign (**=)
num **= 3
print("After **=:", num) 
