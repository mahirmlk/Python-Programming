# Integer to Float Examples
age = 25
height = 170
weight = 65
print(float(age))      # Output: 25.0
print(float(height))   # Output: 170.0
print(float(weight))   # Output: 65.0

# Float to Integer Examples
price = 99.99
temperature = -5.6
pi = 3.14159
print(int(price))      # Output: 99
print(int(temperature))# Output: -5
print(int(pi))        # Output: 3

# String to Integer Examples
year = "2025"
postal_code = "110001"
binary = "1010"
print(int(year))       # Output: 2025
print(int(postal_code))# Output: 110001
print(int(binary, 2))  # Output: 10 (converting binary to decimal)

# Integer to String Examples
score = 950
phone = 1234567890
negative = -42
print(str(score))      # Output: '950'
print(str(phone))      # Output: '1234567890'
print(str(negative))   # Output: '-42'

# String to Float Examples
price = "45.67"
temp = "-12.5"
scientific = "1.23e-4"
print(float(price))    # Output: 45.67
print(float(temp))     # Output: -12.5
print(float(scientific))# Output: 0.000123

# Float to String Examples
gravity = 9.81
percentage = 85.5
small = 0.000001
print(str(gravity))    # Output: '9.81'
print(str(percentage)) # Output: '85.5'
print(str(small))      # Output: '1e-06'

# Boolean to Integer Examples
is_active = True
is_valid = False
print(int(is_active))  # Output: 1
print(int(is_valid))   # Output: 0

# Integer to Boolean Examples
zero = 0
positive = 42
negative = -1
print(bool(zero))      # Output: False
print(bool(positive))  # Output: True
print(bool(negative))  # Output: True

# List to Tuple Examples
names = ['Alice', 'Bob', 'Charlie']
numbers = [1, 2, 3, 4, 5]
mixed = [1, 'hello', True]
print(tuple(names))    # Output: ('Alice', 'Bob', 'Charlie')
print(tuple(numbers))  # Output: (1, 2, 3, 4, 5)
print(tuple(mixed))    # Output: (1, 'hello', True)

# Tuple to List Examples
coords = (10, 20, 30)
colors = ('red', 'green', 'blue')
mixed_tuple = (1, False, 'test')
print(list(coords))    # Output: [10, 20, 30]
print(list(colors))    # Output: ['red', 'green', 'blue']
print(list(mixed_tuple))# Output: [1, False, 'test']