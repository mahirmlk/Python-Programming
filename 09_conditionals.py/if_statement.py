# Basic of If Statement 

# Simple condition check
age = 20

# Single path of execution
if age >= 18:
    print("You are an adult")
    print("You can vote")      # 

# Another example with different condition
temperature = 35

if temperature > 30:
    print("It's a hot day!")

# Multiple conditions can be in separate if statements
score = 85

if score >= 90:
    print("Excellent!")     

if score >= 80:
    print("Good job!")     
