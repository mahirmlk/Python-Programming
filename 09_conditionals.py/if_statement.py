# Basic of If Statement 
# Simple condition check
age = 20

# If statement - executes only when condition is True
if age >= 18:
    print("You are an adult")  # This will execute
    print("You can vote")      # This will also execute

# Another example with different condition
temperature = 35

if temperature > 30:
    print("It's a hot day!")   # This will execute

# Multiple conditions can be in separate if statements
score = 85

if score >= 90:
    print("Excellent!")       # This won't execute

if score >= 80:
    print("Good job!")        # This will execute
